package egovframework.com.uss.umt.service;

public class SendNumberVO  {
	int sendingId;
	String partCode;
	String sendingNumber;
	String sendingOrder;
	public int getSendingId() {
		return sendingId;
	}
	public void setSendingId(int sendingId) {
		this.sendingId = sendingId;
	}
	public String getPartCode() {
		return partCode;
	}
	public void setPartCode(String partCode) {
		this.partCode = partCode;
	}
	public String getSendingNumber() {
		return sendingNumber;
	}
	public void setSendingNumber(String sendingNumber) {
		this.sendingNumber = sendingNumber;
	}
	public String getSendingOrder() {
		return sendingOrder;
	}
	public void setSendingOrder(String sendingOrder) {
		this.sendingOrder = sendingOrder;
	}

	
	
}
