package egovframework.com.msg.moc.service;

public class MoSurveyReceiver {

	private Integer receiver_seq;
	
	private Integer survey_seq;
	
	private String receiver_number;

	
	public Integer getReceiver_seq() {
		return receiver_seq;
	}

	public void setReceiver_seq(Integer receiver_seq) {
		this.receiver_seq = receiver_seq;
	}

	public Integer getSurvey_seq() {
		return survey_seq;
	}

	public void setSurvey_seq(Integer survey_seq) {
		this.survey_seq = survey_seq;
	}

	public String getReceiver_number() {
		return receiver_number;
	}

	public void setReceiver_number(String receiver_number) {
		this.receiver_number = receiver_number;
	}
	
	
}
